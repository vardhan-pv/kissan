-- Profiles policies
CREATE POLICY "profile_select" ON profiles FOR SELECT USING (auth.uid() = id);
CREATE POLICY "profile_insert" ON profiles FOR INSERT WITH CHECK (auth.uid() = id);
CREATE POLICY "profile_update" ON profiles FOR UPDATE USING (auth.uid() = id);

-- Scans policies
CREATE POLICY "scans_all" ON scans FOR ALL USING (auth.uid() = user_id) WITH CHECK (auth.uid() = user_id);

-- Orders policies
CREATE POLICY "orders_all" ON orders FOR ALL USING (auth.uid() = user_id) WITH CHECK (auth.uid() = user_id);

-- Farm crops policies
CREATE POLICY "farm_crops_all" ON farm_crops FOR ALL USING (auth.uid() = user_id) WITH CHECK (auth.uid() = user_id);

-- Farm expenses policies
CREATE POLICY "farm_expenses_all" ON farm_expenses FOR ALL USING (auth.uid() = user_id) WITH CHECK (auth.uid() = user_id);

-- Community posts — public read, own write
CREATE POLICY "posts_select" ON community_posts FOR SELECT USING (true);
CREATE POLICY "posts_insert" ON community_posts FOR INSERT WITH CHECK (auth.uid() = user_id);
CREATE POLICY "posts_update" ON community_posts FOR UPDATE USING (auth.uid() = user_id);

-- Post likes
CREATE POLICY "likes_all" ON post_likes FOR ALL USING (auth.uid() = user_id) WITH CHECK (auth.uid() = user_id);

-- Products — public read
CREATE POLICY "products_read" ON products FOR SELECT USING (true);

-- Reminders
CREATE POLICY "reminders_all" ON reminders FOR ALL USING (auth.uid() = user_id) WITH CHECK (auth.uid() = user_id);

