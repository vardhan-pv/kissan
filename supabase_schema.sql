-- 1. Profiles (extended)
CREATE TABLE IF NOT EXISTS profiles (
  id UUID REFERENCES auth.users ON DELETE CASCADE PRIMARY KEY,
  name TEXT,
  phone TEXT UNIQUE,
  location TEXT,
  avatar TEXT DEFAULT '👨‍🌾',
  role TEXT DEFAULT 'Farmer',
  acres NUMERIC DEFAULT 0,
  soil_type TEXT DEFAULT 'loamy',
  joined TIMESTAMP WITH TIME ZONE DEFAULT now(),
  last_active TIMESTAMP WITH TIME ZONE DEFAULT now(),
  points INTEGER DEFAULT 10,
  level INTEGER DEFAULT 1,
  lang TEXT DEFAULT 'en'
);

-- 2. AI Crop Scans
CREATE TABLE IF NOT EXISTS scans (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id UUID REFERENCES auth.users ON DELETE CASCADE,
  crop_name TEXT,
  image_url TEXT,
  finding TEXT,
  confidence NUMERIC,
  severity TEXT CHECK (severity IN ('none', 'medium', 'high')),
  treatment TEXT,
  growth_stage TEXT,
  days_to_harvest INTEGER,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT now()
);

-- 3. Marketplace Products
CREATE TABLE IF NOT EXISTS products (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name TEXT NOT NULL,
  category TEXT CHECK (category IN ('Seeds', 'Fertilizer', 'Pesticide', 'Tools', 'Equipment')),
  price NUMERIC NOT NULL,
  unit TEXT,
  stock INTEGER DEFAULT 0,
  seller TEXT,
  description TEXT,
  rating NUMERIC DEFAULT 0,
  tag TEXT
);

-- 4. Orders
CREATE TABLE IF NOT EXISTS orders (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id UUID REFERENCES auth.users ON DELETE CASCADE,
  items JSONB,
  total NUMERIC,
  status TEXT CHECK (status IN ('processing', 'shipped', 'delivered', 'cancelled')) DEFAULT 'processing',
  created_at TIMESTAMP WITH TIME ZONE DEFAULT now()
);

-- 5. Farm Crops Tracker
CREATE TABLE IF NOT EXISTS farm_crops (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id UUID REFERENCES auth.users ON DELETE CASCADE,
  crop_name TEXT NOT NULL,
  acres NUMERIC NOT NULL,
  planted_date DATE NOT NULL,
  status TEXT DEFAULT 'growing',
  notes TEXT,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT now()
);

-- 6. Farm Expenses
CREATE TABLE IF NOT EXISTS farm_expenses (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id UUID REFERENCES auth.users ON DELETE CASCADE,
  crop_id BIGINT REFERENCES farm_crops(id),
  item TEXT NOT NULL,
  amount NUMERIC NOT NULL,
  category TEXT CHECK (category IN ('Seeds', 'Fertilizer', 'Pesticide', 'Labor', 'Equipment', 'Other')),
  expense_date DATE DEFAULT CURRENT_DATE,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT now()
);

-- 7. Community Posts
CREATE TABLE IF NOT EXISTS community_posts (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id UUID REFERENCES auth.users ON DELETE CASCADE,
  author_name TEXT,
  author_location TEXT,
  content TEXT NOT NULL,
  post_type TEXT CHECK (post_type IN ('tip', 'question', 'success')) DEFAULT 'tip',
  likes INTEGER DEFAULT 0,
  tags TEXT[],
  created_at TIMESTAMP WITH TIME ZONE DEFAULT now()
);

-- 8. Post Likes (to prevent duplicate likes)
CREATE TABLE IF NOT EXISTS post_likes (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  post_id BIGINT REFERENCES community_posts(id) ON DELETE CASCADE,
  user_id UUID REFERENCES auth.users ON DELETE CASCADE,
  UNIQUE(post_id, user_id)
);

-- 9. Notifications / Reminders
CREATE TABLE IF NOT EXISTS reminders (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id UUID REFERENCES auth.users ON DELETE CASCADE,
  title TEXT NOT NULL,
  note TEXT,
  due_date DATE,
  urgency TEXT CHECK (urgency IN ('high', 'medium', 'upcoming')) DEFAULT 'upcoming',
  done BOOLEAN DEFAULT false,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT now()
);

-- 10. Market Price Cache
CREATE TABLE IF NOT EXISTS market_prices (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  crop_name TEXT NOT NULL,
  price NUMERIC,
  unit TEXT,
  market TEXT,
  change_pct NUMERIC,
  decision TEXT CHECK (decision IN ('sell', 'hold', 'wait')),
  forecast TEXT,
  recorded_at TIMESTAMP WITH TIME ZONE DEFAULT now()
);

